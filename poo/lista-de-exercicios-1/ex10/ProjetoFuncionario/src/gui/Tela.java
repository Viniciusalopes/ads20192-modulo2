    private void jButtonCalcularAumentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularAumentoActionPerformed
        try {
            float salarioAnterior = oreiaSeca.getSalario();
            float percent = Float.parseFloat(jTextFieldPercentual.getText());
            float aumento = oreiaSeca.getAumentoSalarial(percent);
            float novoSalario = salarioAnterior + aumento;
            oreiaSeca.setSalario(novoSalario);
            jTextFieldSalario.setText("" + oreiaSeca.getSalario());

            jTextAreaSaida.setText(String.format(
                    "DADOS DO FUNCIONÁRIO:\n"
                    + "Nome: %s\n"
                    + "Salário Anterior: R$ %.2f\n"
                    + "Aumento: R$ %.2f (%.2f %%)\n"
                    + "Salário Atual: R$ %.2f\n"
                    + "Filho(s): %d\n\n"
                    + "CÁLCULOS:\n"
                    + "INSS: R$ %.2f (%.2f %%)\n"
                    + "Parcela IR: R$ %.2f (%.2f %%)\n"
                    + "Valor Mensal IR: R$ %.2f",
                    oreiaSeca.getNome(),
                    salarioAnterior,
                    aumento, percent,
                    oreiaSeca.getSalario(),
                    oreiaSeca.getFilhos(),
                    oreiaSeca.getInssValor(), oreiaSeca.getInssAliquota(),
                    oreiaSeca.getIrParcela(), oreiaSeca.getIrAliquota(),
                    oreiaSeca.getIrValorMensal()
            ));

            jButtonCalcularInssIr.setVisible(false);
            jButtonAumentoDeSalario.setVisible(true);
            jPanelBotoes.setVisible(true);
            jPanelAumento.setVisible(false);
            jPanelSaida.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonCalcularAumentoActionPerformed

