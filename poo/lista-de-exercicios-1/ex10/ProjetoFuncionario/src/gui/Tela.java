    private void jButtonCalcularInssIrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularInssIrActionPerformed
        try {
            oreiaSeca.setNome(jTextFieldNome.getText());
            oreiaSeca.setSalario(Float.parseFloat(jTextFieldSalario.getText()));
            oreiaSeca.setFilhos(Integer.parseInt(jTextFieldFilhos.getText()));

            jTextAreaSaida.setText(String.format(
                    "DADOS DO FUNCIONÁRIO:\n"
                    + "Nome: %s\n"
                    + "Salário: R$ %.2f\n"
                    + "Filho(s): %d\n\n"
                    + "CÁLCULOS:\n"
                    + "INSS: R$ %.2f (%.2f %%)\n"
                    + "Parcela IR: R$ %.2f (%.2f %%)\n"
                    + "Valor Mensal IR: R$ %.2f",
                    oreiaSeca.getNome(),
                    oreiaSeca.getSalario(),
                    oreiaSeca.getFilhos(),
                    oreiaSeca.getInssValor(), oreiaSeca.getInssAliquota(),
                    oreiaSeca.getIrParcela(), oreiaSeca.getIrAliquota(),
                    oreiaSeca.getIrValorMensal()
            ));
            jTextFieldNome.setEnabled(false);
            jTextFieldSalario.setEnabled(false);
            jTextFieldFilhos.setEnabled(false);

            jButtonCalcularInssIr.setVisible(false);
            jButtonAumentoDeSalario.setVisible(true);
            jButtonNovoFuncionario.setVisible(true);
            jPanelSaida.setVisible(true);
            jTextAreaSaida.grabFocus();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonCalcularInssIrActionPerformed

